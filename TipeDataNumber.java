public class TipeDataNumber {
    public static void main(String[] args) {
        //bilangan bulat
        byte tipeDataByte = 10;
        short tipeDataShort = 10;
        int tipeDataInt = 10;
        long tipeDataLong = 10l; //L besar maupun l kecil tidak masalah hanya utk membedakan Long

        //bilangan pecahan
        float tipeDataFloat = 10.3F; //F besar maupun f kecil tidak masalah hanya utk membedakan float
        double tipeDataDouble = 10.4D; //D besar maupun d kecil tidak masalah hanya utk membedakan double

        int totalGaji = 10_000_000; //pakai _ untuk pemisah tidak boleh titik
    }
}
